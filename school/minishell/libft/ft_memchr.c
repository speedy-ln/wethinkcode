#include "libft.h"

void	*ft_memchr(const void *src, int c, size_t n)
{
	char		tmp;
	char		*tmp2;

	tmp = (unsigned char)c;
	tmp2 = (char*)src;
	while (n--)
	{
		if (*tmp2 == tmp)
		{
			return (tmp2);
		}
		tmp2++;
	}
	return (0);
}
